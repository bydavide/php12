<?php

namespace model;

class {

private $id;
private $decripcion;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getDecripcion()
    {
        return $this->decripcion;
    }

    /**
     * @param mixed $decripcion
     */
    public function setDecripcion($decripcion)
    {
        $this->decripcion = $decripcion;
    }

}
